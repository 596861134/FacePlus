public enum ROOT_URL_TYPE {
    TYPE_DEFAULT("default", UrlRootManager.BASE_URL);

    private String mName;
    private String mUrl;

    ROOT_URL_TYPE(String name, String url) {
        mName = name;
        mUrl = url;
    }

    public String getName() {
        return mName;
    }

    public String getUrl() {
        return mUrl;
    }
}
